////////////////////////////////////////////////////////////////////////////////
// get secure object
////////////////////////////////////////////////////////////////////////////////
window.$secure = function(id) {
	var obj = $(document).find('[id="' + id.substr(1) + '"]');
	if (obj.length >1) {
		console.error('$secure() error! : id="' + id + '" is duplicate.');
		return $([]);
	}
	return obj;
}

